package shared;

public class User
{
  private int id;
  private String username;
  private String password;
  private int securityLevel;



  public int getId()
  {
    return id;
  }

  public String getUsername()
  {
    return username;
  }

  public int getSecurityLevel()
  {
    return securityLevel;
  }

  public String getPassword()
  {
    return password;
  }
}
